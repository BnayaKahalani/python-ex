# Strings in Python are immutable sequences of Unicode code points. Given a string, we need to find its length. Examples:

# Input : 'abc'
# Output : 3

# Input : 'hello world !'
# Output : 13

# Input : ' h e l   l  o '
# Output :14

def find_len(str):
  print(len(str))
  
find_len(" h e l   l  o ")

